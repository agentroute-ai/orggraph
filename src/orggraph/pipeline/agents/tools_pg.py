def _build_pg_dsn() -> str:
    """Construct the default PG_DSN from POSTGRES_* env vars (mirrors the
    pattern used throughout ``orggraph.pipeline``)."""
    return os.environ.get(
        "PG_DSN",
        f"postgresql://{os.environ.get('POSTGRES_USER','orggraph')}:"
        f"{os.environ.get('POSTGRES_PASSWORD','orggraph2026')}@localhost:"
        f"{os.environ.get('POSTGRES_PORT','5432')}/"
        f"{os.environ.get('POSTGRES_DB','orggraph')}",
    )


def default_pg_conn_factory() -> Callable[[], Any]:
    """Returns a callable that yields a fresh psycopg connection with
    pgvector type adapters registered. Imported lazily so non-Postgres
    test paths don't pull in psycopg / pgvector.
    """
    import psycopg
    from pgvector.psycopg import register_vector

    dsn = _build_pg_dsn()

    @contextmanager
    def _open():
        with psycopg.connect(dsn) as conn:
            register_vector(conn)
            yield conn

    return _open


def default_embed_fn() -> Callable[[str], list[float]]:
    """Returns a callable that embeds a single text via the env-configured
    Ollama endpoint (same model as Stage 1, so query vectors live in the
    same space as the indexed corpus)."""
    from openai import OpenAI

    base_url = os.environ.get("EMBED_BASE_URL", "http://localhost:11434/v1")
    api_key = os.environ.get("EMBED_API_KEY", "ollama")
    model = os.environ.get("EMBED_MODEL", "embeddinggemma")
    client = OpenAI(base_url=base_url, api_key=api_key)

    def _embed(text: str) -> list[float]:
        resp = client.embeddings.create(model=model, input=[text])
        return resp.data[0].embedding

    return _embed
